# Ternary Operator
aiman_marks = 90
umme_marks = 97

# if x > y --> do something - print("aiman")
# y > x --> do something else - print("umme")

print("aiman is winner" if aiman_marks > umme_marks else "umme is winner")

if aiman_marks > umme_marks:
    print("aiman is the winner")
else:
    print("umme is the winner")